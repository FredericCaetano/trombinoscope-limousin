-- 1. Personnes
create table if not exists personnes (
  id          text primary key,
  prenom      text not null,
  nom         text not null,
  poste       text,
  telephone   text,
  email       text,
  agence_id   text not null,   -- 'direction' | 'brive' | 'limoges'
  matricule   text,
  ville       text,
  specialites text,
  photo_url   text,            -- URL publique Supabase Storage
  created_at  timestamptz default now()
);

-- 2. Lignes de qualification
create table if not exists qualif_items (
  id        text primary key,
  cat       text not null,
  cat_label text not null,
  numero    text,
  libelle   text not null,
  color     text not null,
  position  integer not null default 0,
  created_at timestamptz default now()
);

-- 3. Attributions personne ↔ qualification
create table if not exists personne_qualifications (
  personne_id text references personnes(id) on delete cascade,
  item_id     text references qualif_items(id) on delete cascade,
  primary key (personne_id, item_id)
);

-- ── Row Level Security ────────────────────────────────────────────
alter table personnes               enable row level security;
alter table qualif_items            enable row level security;
alter table personne_qualifications enable row level security;

-- Lecture publique (tout le monde peut consulter)
create policy "lecture_publique_personnes" on personnes for select using (true);
create policy "lecture_publique_items"     on qualif_items for select using (true);
create policy "lecture_publique_pq"        on personne_qualifications for select using (true);

-- Écriture publique avec clé anon (le mot de passe est géré côté app)
create policy "ecriture_anon_personnes" on personnes for all using (true) with check (true);
create policy "ecriture_anon_items"     on qualif_items for all using (true) with check (true);
create policy "ecriture_anon_pq"        on personne_qualifications for all using (true) with check (true);

